typedef long long ll;
using P = pair<int, int>;

int f(int n)
{
    int sum = 0;
    while (n > 0)
    {
        sum += n % 10;
        n /= 10;
    }
    return sum;
}

int main()
{
    int a, b;
    cin >> a >> b;

    cout << max(f(a), f(b)) << endl;

    return 0;
}
